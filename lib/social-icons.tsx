interface SocialIconProps {
  platform: SocialPlatform;
  style?: SocialIconStyle;
  size?: SocialIconSize;
  primaryColor?: string;
  monochromeColor?: string;
  className?: string;
}

/**
 * Social media icon component that supports brand colors, monochrome, and theme primary styles
 */
export function SocialIcon({
  platform,
  style = "brand",
  size = "medium",
  primaryColor = "#3B82F6",
  monochromeColor = "currentColor",
  className = "",
}: SocialIconProps) {
  const pixelSize = SOCIAL_ICON_SIZES[size];

  // Determine fill color based on style
  let fillColor: string;
  switch (style) {
    case "brand":
      fillColor = SOCIAL_BRAND_COLORS[platform];
      break;
    case "monochrome":
      fillColor = monochromeColor;
      break;
    case "primary":
      fillColor = primaryColor;
      break;
    default:
      fillColor = SOCIAL_BRAND_COLORS[platform];
  }

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={pixelSize}
      height={pixelSize}
      viewBox="0 0 24 24"
      fill={fillColor}
      className={className}
      aria-label={SOCIAL_PLATFORM_LABELS[platform]}
    >
      {SOCIAL_ICON_PATHS[platform]}
    </svg>
  );
}

